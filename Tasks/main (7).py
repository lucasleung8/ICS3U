#roll the chosen dice.
match diceSize:
  case '4':
    rollNumber = (random.randint(1, 4))
    print(f'You rolled a {rollNumber}')
  case '6':
    rollNumber = (random.randint(1, 6))
    print(f'You rolled a {rollNumber}')
  case '8':
     rollNumber = (random.randint(1, 8))
     print(f'You rolled a {rollNumber}')
  case '10':
    rollNumber = (random.randint(1, 10))
    print(f'You rolled a {rollNumber}')
  case '12':
     rollNumber = (random.randint(1, 12))
     print(f'You rolled a {rollNumber}')
  case '20':
    rollNumber = (random.randint(1, 20))
    print(f'You rolled a {rollNumber}')
    if rollNumber == 20:
      print('You win!')
  case _:
    print('Dice not rolled')